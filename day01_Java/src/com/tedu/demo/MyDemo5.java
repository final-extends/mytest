package com.tedu.demo;

/**
 *  Java中的特殊字符
 */
public class MyDemo5 {
    public static void main(String[] args) {
        // \n 换行符
        System.out.print("姓名\n张三\n");

        // \' 单引号
        System.out.println("武汉有个地方叫\'黄鹤楼\'");

        // \" 双引号
        System.out.println("武汉有个地方叫\"江汉路\"");

        // \t 水平制表符
        System.out.println("姓名:\t李良伟");
    }
}
