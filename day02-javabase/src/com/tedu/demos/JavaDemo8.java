package com.tedu.demos;

/**
 *    类型转换：自动类型转换
 */
public class JavaDemo8 {

    public static void main(String[] args) {
        // 声明两个类型的变量
        int number1 = 10;
        double number2;
        // 自动类型转换
        number2 = number1;
        System.out.println("number1="+number1+"\tnumber2="+number2);
    }
}
