package com.tedu.demos;

/**
 *   自增、自减
 */
public class JavaDemo5 {

    public static void main(String[] args) {
        // 声明一个变量并赋值
        int count = 0;
        // 让 count 变量中的值自加1
        // count++;
        ++count;  // 等同于 count = count + 1;
        // 输出显示 count 的结果
        System.out.println("count = "+count);
    }
}
